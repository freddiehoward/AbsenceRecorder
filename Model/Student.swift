import Foundation

class Student {
    
    let firstName: String
    let lastName: String
    let birthday: String

    init(firstName: String, lastName: String, birthday: String) {
        self.firstName = firstName
        self.lastName = lastName
        self.birthday = birthday
    }
    
}
